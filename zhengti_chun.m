function dy = zhengti_chun(t, y)

P = y(15);
L = y(16);
beta = y(17:20);

%UNTITLED2 Summary of this function goes here
%   Detailed explanation goes here
% y1 饺子中心温度  y10 9结点温度  ys 饺子外表温度  y11内壁温度  y12 外壁温度   y13 水温  a3=1.6*10^(-7)
% Bi=h'r/j4 h'=4518 r=0.01 j4=0.55 
%Q5=1600 外加热  
%Q1 水and内壁   a1=335.2 水and内壁导热系数  A1=0.8L  L=0.08   A2=0.05
%Q(dumplin) 饺子吸热  c‘=2.85  m=10/2000
%Q3.2 水and空气  a2.1=2.6 a2.2 水对空气  M=5kg 水质量 cp1水比热容 4200
%Q2 锅壁导热  Ri外壁热阻  Ri'锅底热阻  Q3.1锅外壁and空气 c'锅的比热容  M'锅的质量  
dy=zeros(20,1);
%%dy(1) = 300*1.6*10^(-7)/0.01^2*(y(2)-y(1));
%dy1=300*a3*(y2-y1)/r(dumplin)**
%%dy(2) = 100*1.6*10^(-7)/0.01^2*(5/3*y(3)-2*y(2)+1/3*y(1));
%%dy(3) = 100*1.6*10^(-7)/0.01^2*(7/5*y(4)-2*y(3)+3/5*y(2));
%%dy(4) = 100*1.6*10^(-7)/0.01^2*(9/7*y(5)-2*y(4)+5/7*y(3));
%%dy(5) = 100*1.6*10^(-7)/0.01^2*(11/9*y(6)-2*y(5)+7/9*y(4));
%%dy(6) = 100*1.6*10^(-7)/0.01^2*(13/11*y(7)-2*y(6)+9/11*y(5));
%%dy(7) = 100*1.6*10^(-7)/0.01^2*(15/13*y(8)-2*y(7)+11/13*y(6));
%%dy(8) = 100*1.6*10^(-7)/0.01^2*(17/15*y(9)-2*y(8)+13/15*y(7));
%%dy(9) = 100*1.6*10^(-7)/0.01^2*(19/17*y(10)-2*y(9)+15/17*y(8));
%dyi=100*a3*((2i+3/2i+1)y(i+1)-2yi+(2i-1/2i+1)y(i-1))
%%dy(10)= 100*1.6*10^(-7)/0.01^2*(160/57*(4518/55*(y(13)+273)+20*y(10))/(20+4518/55)-4*y(10)+68/57*y(9));
%dy10=100a3/r(dumplin)**(160/57*ys-4y10+68/57*y9)
%ys=(Bi*y13+20y10)/(20+Bi)
dy(11)=(beta(1)*(y(13)-y(11))-(166.9*(y(11)-y(12))))/(500*1.047/2);
%dy11=(Q1-Q2)/c''M',beta(1)=a1水与锅内壁
dy(12)=((166.9*(y(11)-y(12)))-(beta(2)*(y(12)-20)^(5/4)/((y(12)+20)/2+273)^(0.25)))/(500*1.047/2);
%dy12=（Q2-Q3.1）/c''M',beta(2)=a2.1空气与锅外壁
%Q2=Q2.1+Q2.2   Q2.1=(y11-y12)/Ri  Q2.2=(y11-y12)/Ri'
%Q3.1=a2.1*s   s=外壁
dy(13)=(P-(beta(2)*(y(12)-20)^(5/4)/((y(12)+20)/2+273)^(0.25))-(beta(3)*0.54*(y(13)-20)^(5/4)/((y(13)+20)/2+273)^(0.25))-0.7*beta(4)*exp(0.04928*y(13))*(y(13)-20)^(1/3))/(4200*L);
%dy13=(Q5-Q3.1-Q3.2-Q水)/(cp1M),beta(3)=a2.2水与空气
%Q3.2=a2.2*s*(y13-ya)/4=*(y13-ya)^(5/4)/T^(1/4)   ！！（此为温差传能）
%Q水  此为蒸发传能
%Q(dumplin)=c’mdy6
dy(14)=beta(4)*exp(0.04928*y(13))*(y(13)-20)^(1/3)/(-1.476*10^(-5)*y(13)^3+0.0008026*y(13)^2-2.265*y(13)+2491)/2000;
%此为水的传质=Q/水的C,beta(4)=a 蒸发系数
dy(11:14)
end

